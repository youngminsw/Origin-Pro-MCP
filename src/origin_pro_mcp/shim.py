def _real_registry() -> dict:
    """The real FastMCP tool registry — ``{name: fn}`` for all 37 tools."""
    from . import server  # noqa: F401 — importing registers every tool
    from .app import mcp as real_mcp

    return {name: tool.fn for name, tool in real_mcp._tool_manager._tools.items()}


def _make_forwarder(tool_name: str, real_fn, client: ShimClient):
    """A forwarder that is schema-identical to ``real_fn`` but calls the daemon.

    ``functools.wraps`` copies ``__doc__``/``__name__``/``__annotations__``/etc;
    an explicit ``__signature__`` guarantees FastMCP generates a byte-identical
    JSON schema (so MCP clients can't tell the shim from the in-process server).
    """
    sig = inspect.signature(real_fn)

    @functools.wraps(real_fn)
    def forwarder(*args, **kwargs):
        bound = sig.bind(*args, **kwargs)
        return client.call(tool_name, dict(bound.arguments))

    forwarder.__signature__ = sig
    forwarder.__name__ = real_fn.__name__
    forwarder.__doc__ = real_fn.__doc__
    forwarder.__annotations__ = dict(getattr(real_fn, "__annotations__", {}))
    return forwarder


def build_shim_server(client: ShimClient, *, name: str = "origin-pro") -> FastMCP:
    """Build a fresh shim ``FastMCP`` whose tools forward through ``client``."""
    shim = FastMCP(name, log_level="ERROR")
    for tool_name, real_fn in _real_registry().items():
        shim.add_tool(_make_forwarder(tool_name, real_fn, client),
                      name=tool_name)
    return shim


_default_client: Optional[ShimClient] = None
_default_client_lock = threading.Lock()


def _get_default_client() -> ShimClient:
    global _default_client
    with _default_client_lock:
        if _default_client is None:
            _default_client = ShimClient()
        return _default_client


def run_stdio() -> None:
    """Run the shim's FastMCP stdio loop (the daemon-backed entrypoint).

    The heartbeat thread is a daemon thread, so process exit on stdin EOF is
    clean without an explicit join.
    """
    build_shim_server(_get_default_client()).run(transport="stdio")
